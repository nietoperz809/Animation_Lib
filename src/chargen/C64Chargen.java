    /**
     * Draws one row of a character into character bitmap
     * @param charidx the bits of a row 
     * @param x Position in Bitmap
     * @param y Position in Bitmap
     */
    private void drawRow (int charidx, int x, int y)
    {
    	int c = c64Chargen[charidx];
        int i = 128;
        for (int s = 0; s < WIDTH; s++)
        {
            if ((c & i) == i)
            	charBitmap.setPixel (s+x, y, pixelColor);
            else
            	charBitmap.setPixel (s+x, y, bckGround);
            i >>>= 1;
        }
    }

    /**
     * Set copy position in destination bitmap of this Text Bitmap  
     * @param x XPos
     * @param y YPos
     */
    public void setOrigin (int x, int y)
    {
        drawPos.x = x;
        drawPos.y = y;
    }
    
    /**
     * Creates Bitmap and draws whole line of characters
     * @param charidx array of start positions in chargen array
     */
    public void printLine (int... charidx)
    {
    	charBitmap = Bitmap.createBitmap ((WIDTH+1)*charidx.length, 
        									HEIGHT, 
        									Bitmap.Config.ARGB_8888);
    	for (int n=0; n<charidx.length; n++)
    	{
    		for (int s = 0; s < HEIGHT; s++)
    		{
    			drawRow (charidx[n]+s, (WIDTH+1)*n, s);        
    		}
    	}
    	
    	charBitmap = Bitmap.createScaledBitmap(charBitmap, 
    			charidx.length*scale, scale, false);
    }

    /**
     * Prints string into bitmap using C64 character set 
     * @param line
     */
    public void printLine (String line)
    {
    	int arr[] = new int[line.length()];
    	for (int s=0; s<line.length(); s++)
    	{
    		Integer i = map.get(line.charAt(s));
    		if (i == null)
    			arr[s] = 0x968;  // (-) if not found
    		else	
    			arr[s] = i;
    	}
    	printLine (arr);
    }
    
